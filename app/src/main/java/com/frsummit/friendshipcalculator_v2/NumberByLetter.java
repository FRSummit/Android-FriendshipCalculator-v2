package com.frsummit.friendshipcalculator_v2;

public class NumberByLetter {

    public int getNumberByLetter(String ch) {
        ch = ch.toLowerCase();
        int no = 0;
        switch (ch) {
            case "a":
                no = 65;
                break;
            case "b":
                no = 66;
                break;
            case "c":
                no = 67;
                break;
            case "d":
                no = 68;
                break;
            case "e":
                no = 69;
                break;
            case "f":
                no = 70;
                break;
            case "g":
                no = 71;
                break;
            case "h":
                no = 72;
                break;
            case "i":
                no = 73;
                break;
            case "j":
                no = 74;
                break;
            case "k":
                no = 75;
                break;
            case "l":
                no = 76;
                break;
            case "m":
                no = 77;
                break;
            case "n":
                no = 78;
                break;
            case "o":
                no = 79;
                break;
            case "p":
                no = 80;
                break;
            case "q":
                no = 81;
                break;
            case "r":
                no = 82;
                break;
            case "s":
                no = 83;
                break;
            case "t":
                no = 84;
                break;
            case "u":
                no = 85;
                break;
            case "v":
                no = 86;
                break;
            case "w":
                no = 87;
                break;
            case "x":
                no = 88;
                break;
            case "y":
                no = 89;
                break;
            case "z":
                no = 90;
                break;
            default:
                break;
        }
        return no;
    }
}
